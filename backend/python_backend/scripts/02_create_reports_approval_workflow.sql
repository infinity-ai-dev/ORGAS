-- Main Approval Workflow Tables
-- ============================================================================

-- Table: relatorios_em_aprovacao
-- Purpose: Holds reports pending user approval
-- Stores the complete JSON response from agent for processing
CREATE TABLE IF NOT EXISTS public.relatorios_em_aprovacao (
    id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    request_id VARCHAR(255) NOT NULL UNIQUE,
    task_id VARCHAR(255),
    report_id VARCHAR(255),

    -- Client & Context
    cliente_id VARCHAR(100) NOT NULL,
    cliente_nome VARCHAR(255) NOT NULL,

    -- Report Metadata
    tipo_parecer public.parecer_type_enum NOT NULL,
    data_geracao TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,

    -- Agent Response Data (JSON structure varies by tipo_parecer)
    response_data JSONB NOT NULL,  -- Complete structured response from agent

    -- Document Metrics
    documentos_analisados INT DEFAULT 0,
    etapas_executadas INT DEFAULT 0,

    -- Tracking
    criado_em TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    vencimento_em TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP + INTERVAL '7 days',

    -- Status
    status_aprovacao public.approval_status_enum DEFAULT 'pendente',

    -- Indexes
    CONSTRAINT valid_cliente_id CHECK (cliente_id != ''),
    CONSTRAINT valid_cliente_nome CHECK (cliente_nome != ''),
    CONSTRAINT valid_response_data CHECK (response_data IS NOT NULL)
);

CREATE INDEX idx_relatorios_em_aprovacao_cliente_id ON public.relatorios_em_aprovacao(cliente_id);
CREATE INDEX idx_relatorios_em_aprovacao_tipo_parecer ON public.relatorios_em_aprovacao(tipo_parecer);
CREATE INDEX idx_relatorios_em_aprovacao_data_geracao ON public.relatorios_em_aprovacao(data_geracao DESC);
CREATE INDEX idx_relatorios_em_aprovacao_vencimento ON public.relatorios_em_aprovacao(vencimento_em);
CREATE INDEX idx_relatorios_em_aprovacao_status ON public.relatorios_em_aprovacao(status_aprovacao);

-- Table: relatorios_aprovados
-- Purpose: Holds approved reports for archival/retrieval
CREATE TABLE IF NOT EXISTS public.relatorios_aprovados (
    id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    relatorio_original_id UUID NOT NULL REFERENCES public.relatorios_em_aprovacao(id) ON DELETE RESTRICT,

    -- Approval Metadata
    aprovado_por VARCHAR(255) NOT NULL,
    data_aprovacao TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    observacoes_aprovacao TEXT,

    -- Report Data
    response_data JSONB NOT NULL,  -- Copy of approved response

    -- HTML Output (pre-generated by agent to avoid rework)
    html_output TEXT,

    -- Tracking
    criado_em TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    atualizado_em TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,

    -- Indexes
    CONSTRAINT valid_aprovado_por CHECK (aprovado_por != '')
);

CREATE INDEX idx_relatorios_aprovados_original_id ON public.relatorios_aprovados(relatorio_original_id);
CREATE INDEX idx_relatorios_aprovados_data_aprovacao ON public.relatorios_aprovados(data_aprovacao DESC);
CREATE INDEX idx_relatorios_aprovados_aprovado_por ON public.relatorios_aprovados(aprovado_por);

-- Table: relatorios_reprovados
-- Purpose: Holds rejected reports with feedback for model training
CREATE TABLE IF NOT EXISTS public.relatorios_reprovados (
    id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    relatorio_original_id UUID NOT NULL REFERENCES public.relatorios_em_aprovacao(id) ON DELETE RESTRICT,

    -- Rejection Metadata
    reprovado_por VARCHAR(255) NOT NULL,
    data_rejeicao TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,

    -- Primary Rejection Reason
    motivo_rejeicao public.rejeicao_motivo_enum NOT NULL,
    justificativa TEXT NOT NULL,

    -- Detailed Error Information (for model training)
    secoes_com_erro TEXT[],  -- Array of section names with errors
    campo_com_erro VARCHAR(255),  -- Specific field that has error
    valor_esperado VARCHAR(255),
    valor_recebido VARCHAR(255),

    -- Response Data (for comparison)
    response_data JSONB NOT NULL,

    -- Tracking
    criado_em TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    atualizado_em TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,

    -- Indexes
    CONSTRAINT valid_reprovado_por CHECK (reprovado_por != ''),
    CONSTRAINT valid_justificativa CHECK (justificativa != '')
);

CREATE INDEX idx_relatorios_reprovados_original_id ON public.relatorios_reprovados(relatorio_original_id);
CREATE INDEX idx_relatorios_reprovados_motivo ON public.relatorios_reprovados(motivo_rejeicao);
CREATE INDEX idx_relatorios_reprovados_data_rejeicao ON public.relatorios_reprovados(data_rejeicao DESC);
CREATE INDEX idx_relatorios_reprovados_reprovado_por ON public.relatorios_reprovados(reprovado_por);

