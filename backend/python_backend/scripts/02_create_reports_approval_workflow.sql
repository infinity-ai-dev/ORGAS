-- ============================================================================
-- Enum Types for Approval Workflow
-- ============================================================================

CREATE TYPE public.approval_status_enum AS ENUM (
    'pendente',
    'aprovado',
    'reprovado'
);

CREATE TYPE public.parecer_type_enum AS ENUM (
    'pessoal',
    'fiscal',
    'contabil',
    'atendimento'
);

CREATE TYPE public.rejeicao_motivo_enum AS ENUM (
    'dados_inconsistentes',
    'calculo_errado',
    'campo_faltante',
    'interpretacao_incorreta',
    'dados_incompletos',
    'formato_invalido'
);

CREATE TYPE public.feedback_prioridade_enum AS ENUM (
    'critica',
    'alta',
    'media',
    'baixa'
);

CREATE TYPE public.feedback_status_enum AS ENUM (
    'novo',
    'em_analise',
    'corrigido',
    'nao_aplicavel'
);

