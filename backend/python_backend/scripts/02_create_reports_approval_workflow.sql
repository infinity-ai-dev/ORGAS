    id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    relatorio_id UUID NOT NULL,  -- References either aprovacao/aprovado/reprovado ID

    -- State Transition
    acao VARCHAR(100) NOT NULL CHECK (acao IN (
        'criado',
        'em_aprovacao',
        'aprovado',
        'reprovado',
        'data_alterada',
        'observacoes_alteradas'
    )),

    -- User & Session
    usuario_id VARCHAR(255) NOT NULL,
    usuario_nome VARCHAR(255),

    -- Details
    detalhes JSONB DEFAULT '{}',  -- Specific changes made

    -- Timestamps
    criado_em TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,

    -- Indexes
    CONSTRAINT valid_usuario CHECK (usuario_id != '')
);

CREATE INDEX idx_auditoria_relatorios_relatorio_id ON public.auditoria_relatorios(relatorio_id);
CREATE INDEX idx_auditoria_relatorios_acao ON public.auditoria_relatorios(acao);
