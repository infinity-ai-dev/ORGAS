-- Table: feedback_treinamento
-- Purpose: Aggregate rejection feedback for model fine-tuning analysis
CREATE TABLE IF NOT EXISTS public.feedback_treinamento (
    id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    tipo_parecer public.parecer_type_enum NOT NULL,

    -- Error Classification
    motivo_rejeicao public.rejeicao_motivo_enum NOT NULL,
    campo_com_erro VARCHAR(255) NOT NULL,
    secao VARCHAR(255),  -- Which section of the report (e.g., "Secao1_Faturamento")

    -- Error Pattern Tracking
    frequencia_erro INT DEFAULT 1,  -- Number of times this error occurred
    frequencia_atualizada_em TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,

    -- Context for Training
    valor_esperado VARCHAR(255),
    valor_recebido VARCHAR(255),
    descricao_padrao_erro TEXT,

    -- Priority & Status
    prioridade public.feedback_prioridade_enum DEFAULT 'media',
    status public.feedback_status_enum DEFAULT 'novo',

    -- Tracking
    primeira_ocorrencia TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    ultima_ocorrencia TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    criado_em TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    atualizado_em TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,

    -- Notes for ML team
    notas_ml TEXT,

    -- Indexes for analysis
    CONSTRAINT valid_campo CHECK (campo_com_erro != '')
);

CREATE INDEX idx_feedback_tipo_parecer ON public.feedback_treinamento(tipo_parecer);
CREATE INDEX idx_feedback_motivo ON public.feedback_treinamento(motivo_rejeicao);
CREATE INDEX idx_feedback_campo ON public.feedback_treinamento(campo_com_erro);
CREATE INDEX idx_feedback_secao ON public.feedback_treinamento(secao);
CREATE INDEX idx_feedback_prioridade ON public.feedback_treinamento(prioridade);
CREATE INDEX idx_feedback_status ON public.feedback_treinamento(status);
CREATE INDEX idx_feedback_frequencia ON public.feedback_treinamento(frequencia_erro DESC);

